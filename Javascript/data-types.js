// Data Types - Primitive
// Number
let length = 20;
length = 20.1;
length = -10;
console.log(length);

// strings - " " or ' '
let greeting = "Let's go!";
// let greeting = 'Let's go!'; - will cause an error
console.log(greeting);

// boolean - true or false
let isDone = false;
console.log(isDone);

// undefined - uninitialized or missing value
let city;
console.log(city);

// null - intentional absence of an object value
let car = null;
console.log(car);